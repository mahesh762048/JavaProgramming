public class CWH_21_Ch5_While_loops {
    public static void main(String[] args) {
        int i = 100;
        System.out.println("Print Natural Number From 100 to 200");
        while (i<=200) // While condition is true then only one time loop Executed one Time
        {
            System.out.println(i);
            i++;
        }
        System.out.println("Finish Running While Loop..!!");
      }
}
